let paletteId = 0
const palettes = [
	PAL.DB32,
	PAL.EDG32,
	PAL.ARNE32,
	PAL.GAMEBOY4,
]

class Palette {
	static get palette (){
		return palettes[paletteId]
	}
	static get prevPalette (){
		paletteId = clamp(--paletteId, 0, palettes.length-1)
		return Palette.palette
	}
	static get nextPalette (){
		paletteId = clamp(++paletteId, 0, palettes.length-1)
		return Palette.palette
	}
}


export default Palette